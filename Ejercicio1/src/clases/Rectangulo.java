public class Rectangulo {
    public float base;
    private float altura;
    /**
     * Default constructor
     */
    public Rectangulo() {
    }


    public Rectangulo(float base, float altura) {
    }


    /**
     * 
     */

    public float getBase() {
        return base;
    }

    public void setBase(float base) {
        this.base = base;
    }

    public float getAltura() {
        return altura;
    }

    public void setAltura(float altura) {
        this.altura = altura;
    }
}